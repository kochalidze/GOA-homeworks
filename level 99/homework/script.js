// ### **1. მარტივი მისალმების ფუნქცია**
// შექმენით iarrow functon, რომელიც იღებს ერთი პარამეტრს (სახელი) და აბრუნებს „გამარჯობა, [სახელი]!“. 
// თუ პარამეტრი არ არის მოცემული, გამოიყენეთ "მაყურებელი" როგორც დეფოლტი.

const greet = (name = 'მაყურებელი') => `გამარჯობა, ${name}`
console.log(greet())

// ### **2. ორი რიცხვის ჯამი**
// შექმენით arrow function, რომელიც იღებს ორი პარამეტრს (a, b) და აბრუნებს მათ ჯამს.
//  თუ პარამეტრები არ არის მოცემული, გამოიყენეთ `0` და `5` როგორც დეფოლტი მნიშვნელობები.

const nums = (a=0, b=5) => {
    return a + b
}
console.log(nums(5, 10))
console.log(nums(5))
console.log(nums())

// ### **3. მაქსიმალური რიცხვი**
// შექმენით arrow function, რომელიც იღებს ორი რიცხვს და აბრუნებს მეტს მათ შორის.
//  თუ პარამეტრები არ არის მოცემული, გამოიყენეთ `10` და `20` როგორც დეფოლტი მნიშვნელობები.

const numbers = (num1=10, num2=20) => {
    if (num1 > num2) {
        return `${num1} მეტია ${num2}-ზე`
    }else {
        return `${num2} მეტია ${num1}-ზე`
    };
};
console.log(numbers(20, 19));
console.log(numbers(123));
console.log(numbers());

// ### **4. სამი რიცხვის გამრავლება**
// შექმენით arrow function, რომელიც იღებს სამი პარამეტრს და აბრუნებს მათ გამრავლებას.
//  თუ პარამეტრები არ არის მოცემული, გამოიყენეთ `1`, `2` და `3` როგორც დეფოლტი მნიშვნელობები.

const numbers2 = (a1=1, b1=2, c1=3) => {
    return a1 * b1 * c1
};
console.log(numbers2(21,312,212));
console.log(numbers2(21,23));
console.log(numbers2());

// ### **5. ფარმაცევტული ფასის გამოთვლა**
// შექმენით arrow function, რომელიც იღებს ორ პარამეტრს (ფასს და გადასახადს) და აბრუნებს საბოლოო ფასს (ფასი + გადასახადი). თუ გადასახადი არ არის მოცემული, გამოიყენეთ `5%` როგორც დეფოლტი.

const priceTax = (price, tax = 0.05) => {
    return price + tax
};
console.log(priceTax(100, 0.1)); 
console.log(priceTax(100));  

// ### **6. დაბოლოების სიმრავლე**
// შექმენით arrow function, რომელიც იღებს ტექსტს და რიცხვს და აბრუნებს ტექსტის გამეორებას მოცემული რიცხვის რაოდენობის მიხედვით. თუ რიცხვი არ არის მოცემული, გამოიყენეთ `3` როგორც დეფოლტი.

const stringNum = (str, number11=3) => {
    return str.repeat(number11)
};
console.log(stringNum('Giga', 11));

// ### **7. რიცხვების ამოწმება**
// შექმენით arrow function, რომელიც იღებს ორი რიცხვს და აბრუნებს true თუ პირველი რიცხვი მეტია მეორეზე. თუ არ არის პარამეტრები, გამოიყენეთ `5` და `10` როგორც დეფოლტი.

const numbers12 = (num1=5, num2=10) => {
    if (num1 > num2) {
        return true
    }else {
        return false
    };
};
console.log(numbers12(20, 19));
console.log(numbers12(123));
